package ru.izhxx.aichallenge.domain.model.reminder

/**
 * Доменная модель результата выполнения задачи-напоминания.
 *
 * Хранит итог выполнения (успех/ошибка), текст ответа для пользователя
 * и (опционально) трассировку вызовов инструментов.
 */
data class ReminderResult(
    /**
     * Идентификатор результата. Null для ещё не сохранённых записей.
     */
    val id: Long? = null,

    /**
     * Идентификатор задачи, к которой относится результат.
     */
    val taskId: Long,

    /**
     * Метка времени выполнения (мс epoch).
     */
    val runAt: Long,

    /**
     * Итоговый статус выполнения.
     */
    val status: ReminderStatus,

    /**
     * Текст ответа, который должен увидеть пользователь.
     */
    val responseText: String,

    /**
     * Сырые данные/трассировка вызовов инструментов (JSON), если есть.
     */
    val rawToolTrace: String? = null,

    /**
     * Описание ошибки, если выполнение завершилось неуспешно.
     */
    val errorMessage: String? = null
)
