package ru.izhxx.aichallenge.domain.model.reminder

/**
 * Статус выполнения задачи-напоминания.
 *
 * Используется для фиксации результата выполнения и отображения истории.
 */
enum class ReminderStatus {
    /**
     * Задача выполнена успешно, результат получен.
     */
    SUCCESS,

    /**
     * Во время выполнения произошла ошибка (LLM/MCP/сеть/БД и т.п.).
     */
    ERROR
}
