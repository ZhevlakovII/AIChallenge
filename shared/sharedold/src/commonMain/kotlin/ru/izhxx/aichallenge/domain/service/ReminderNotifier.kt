package ru.izhxx.aichallenge.domain.service

/**
 * Платформозависимый интерфейс отправки системных уведомлений о выполнении напоминаний.
 *
 * Реализации должны показать уведомление и по нажатию перевести пользователя
 * на экран задачи/конкретного результата (если поддерживается диплинк).
 */
interface ReminderNotifier {
    /**
     * Показать уведомление о новом результате выполнения напоминания.
     *
     * @param taskId идентификатор задачи
     * @param resultId идентификатор результата (для навигации)
     * @param title заголовок уведомления (обычно имя задачи)
     * @param preview краткий текст результата (превью)
     */
    fun notifyResult(taskId: Long, resultId: Long, title: String, preview: String)
}
