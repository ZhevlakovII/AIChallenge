package ru.izhxx.aichallenge.domain.model.reminder

/**
 * Единица периода для задачи-напоминания.
 *
 * Определяет, через какие интервалы времени должна запускаться задача.
 */
enum class ReminderPeriodUnit {
    /**
     * Период указывается в минутах.
     */
    MINUTES,

    /**
     * Период указывается в часах.
     */
    HOURS,

    /**
     * Период указывается в днях.
     */
    DAYS
}
