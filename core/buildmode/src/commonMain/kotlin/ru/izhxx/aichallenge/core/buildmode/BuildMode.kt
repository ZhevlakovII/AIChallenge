package ru.izhxx.aichallenge.core.buildmode

/**
 * Определяет, является ли текущая сборка отладочной (debug).
 *
 * Назначение:
 * - Позволяет различать debug и release сборки на уровне приложения.
 * - Используется для включения/выключения debug-функциональности
 * (логирование, отладочные инструменты).
 * - Реализовано через expect/actual для поддержки всех платформ (Android, iOS, JVM, Desktop).
 *
 * Платформенные реализации:
 * - Android: использует BuildConfig.DEBUG (в будущем, сейчас hardcoded true)
 * - JVM/Desktop: проверяет system property или возвращает константу
 * - iOS: определяет через директивы компилятора
 *
 * Правила использования:
 * - Вызывайте эту функцию для условного включения debug-функциональности.
 * - Не используйте для критических проверок безопасности (можно подменить на устройстве).
 * - Предпочитайте статическую проверку во время компиляции, где возможно.
 *
 * Пример:
 * ```kotlin
 * if (isDebugBuild()) {
 *     log(LogLevel.DEBUG, Tag.of("App"), { "Debug info: $data" })
 * }
 * ```
 *
 * @return true если текущая сборка является debug, false если release.
 */
expect fun isDebugBuild(): Boolean
