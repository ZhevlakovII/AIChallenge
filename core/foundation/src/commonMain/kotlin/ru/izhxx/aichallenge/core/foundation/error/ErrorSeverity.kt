package ru.izhxx.aichallenge.core.foundation.error

/**
 * Уровень серьёзности ошибки.
 */
enum class ErrorSeverity {

    /** Информационное событие; операция завершена, возможен ненавязчивый сигнал в UI. */
    Info,

    /** Предупреждение; возможны незначительные проблемы, UI — баннер/подсказка. */
    Warning,

    /** Ошибка; операция не выполнена, UI должен отобразить понятное сообщение. */
    Error,

    /** Критический сбой; возможна деградация функционала, требуется немедленный отклик. */
    Critical
}
