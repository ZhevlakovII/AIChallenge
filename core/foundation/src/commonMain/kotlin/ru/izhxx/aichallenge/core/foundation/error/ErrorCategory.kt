package ru.izhxx.aichallenge.core.foundation.error

/**
 * Категории ошибок. Стабильный набор для ABI.
 */
enum class ErrorCategory {

    /** Сетевой уровень: проблемы соединения, DNS/TLS, транспорт. */
    Network,

    /** Уровень HTTP-протокола: коды статуса, методы, URL. */
    Http,

    /** Сериализация/десериализация: парсинг, несовместимость схем. */
    Serialization,

    /** Валидация входных данных или нарушенный контракт. */
    Validation,

    /** Подсистема хранения/IO: файлы, БД, транзакции. */
    Storage,

    /** Аутентификация: токен, логин/пароль, сессия. */
    Auth,

    /** Авторизация/права доступа. */
    Permission,

    /** Таймаут выполнения операции. */
    Timeout,

    /** Превышение лимитов частоты/квоты. */
    RateLimit,

    /** Доменные ошибки бизнес-логики. */
    Domain,

    /** Непредвиденная/немаппленная ситуация. */
    Unknown
}
